package citadels.model.card;

/**
 * All possible district colours in Citadels.
 * They correspond to the five coloured gems printed on the cards
 * and are used by character abilities and scoring bonuses.
 */
public enum DistrictColor {
    YELLOW,  // Noble
    BLUE,    // Religious
    GREEN,   // Trade
    RED,     // Military
    PURPLE   // Unique (special-ability) districts
}
